package adm;

public class ExamscoreBeans {
	
		private String usrid ;
		private String name ; 
		private int score ; 
		private String tech ; 
		private String sem ; 
		
		public void ExamscoreBeans() {}

		public String getUsrid() {
			return usrid;
		}

		public void setUsrid(String usrid) {
			this.usrid = usrid;
		}

		public String getName() {
			return name;
		}

		public void setName(String name) {
			this.name = name;
		}


		public int getScore() {
			return score;
		}

		public void setScore(int score) {
			this.score = score;
		}

		public String getTech() {
			return tech;
		}

		public void setTech(String tech) {
			this.tech = tech;
		}

		public String getSem() {
			return sem;
		}

		public void setSem(String sem) {
			this.sem = sem;
		}
		
		
		

}

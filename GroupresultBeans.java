package adm;

public class GroupresultBeans {

	private String project_name ; 
	private String usr1 , usr2 , usr3 ; 
	private String tech ; 
	private int ranked ;
	private String sem ; 
	
	public void GroupresultBeans() {}

	public String getProject_name() {
		return project_name;
	}

	public void setProject_name(String project_name) {
		this.project_name = project_name;
	}

	public String getUsr1() {
		return usr1;
	}

	public void setUsr1(String usr1) {
		this.usr1 = usr1;
	}

	public String getUsr2() {
		return usr2;
	}

	public void setUsr2(String usr2) {
		this.usr2 = usr2;
	}

	public String getUsr3() {
		return usr3;
	}

	public void setUsr3(String usr3) {
		this.usr3 = usr3;
	}

	public String getTech() {
		return tech;
	}

	public void setTech(String tech) {
		this.tech = tech;
	}

	public int getRanked() {
		return ranked;
	}

	public void setRanked(int ranked) {
		this.ranked = ranked;
	}

	public String getSem() {
		return sem;
	}

	public void setSem(String sem) {
		this.sem = sem;
	}
	
}
